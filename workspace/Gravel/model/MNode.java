package model;

/**
 * A mathematical Node only contains the name and its index
 *
 * @author ronny
 */
public class MNode {
  public int index;
  public String name;

  public MNode(int i, String n) {
    index = i;
    name = n;
  }

  public MNode clone() {
    return new MNode(index, new String(name.toCharArray()));
  }
}
